#função de soma
def somar(a,b):
    return float(a) + float(b)

#função de subtração
def sutrair (a,b):
    return float(a) - float(b)

#função divisão
def dividir (a,b):
    if b ==0:
        return 0
    return float(a) / float(b)

#função de multiplicar
def multiplicar(a,b):
    return float(a) * float(b)